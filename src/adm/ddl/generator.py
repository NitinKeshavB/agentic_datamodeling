# Map source/Databricks types to compact Mermaid-friendly type tokens
_MERMAID_TYPE_MAP: dict[str, str] = {
    "bigint": "bigint",
    "int": "int",
    "integer": "int",
    "smallint": "int",
    "tinyint": "int",
    "long": "bigint",
    "double": "float",
    "float": "float",
    "decimal": "decimal",
    "numeric": "decimal",
    "string": "string",
    "varchar": "string",
    "char": "string",
    "text": "string",
    "boolean": "boolean",
    "bool": "boolean",
    "timestamp": "timestamp",
    "timestamp_ntz": "timestamp",
    "date": "date",
    "binary": "binary",
}


def _mermaid_type(raw_type: str) -> str:
    """Map a source/Databricks type string to a compact Mermaid-friendly token."""
    base = raw_type.lower().split("(")[0].strip()
    return _MERMAID_TYPE_MAP.get(base, raw_type.lower())


def _safe_entity(name: str) -> str:
    """Replace spaces/hyphens so Mermaid parses entity names correctly."""
    return re.sub(r"[^A-Za-z0-9_]", "_", name)


def generate_mermaid_er_diagram(report: dict[str, Any]) -> str:
    """Generate a Mermaid erDiagram from a discovery report.

    Returns a Markdown string containing:
    - A fenced ```mermaid erDiagram block (renders in GitHub, VS Code, mermaid.live)
    - A Tables summary table (name, column count, PK, FK counts)
    - A Relationships table (child/parent table+column, type, confidence)
    - An AI Analysis section (recommendations, data quality, logical model, physical model)
      rendered only when the report contains a parsed ai_analysis dict.
    """
    tables = report.get("tables", [])
    relationships = report.get("relationships", [])

    # Build lookup structures
    tables_by_name = {t["name"]: t for t in tables}
    pk_map: dict[str, str] = {}
    for t in tables:
        pk_col = t["primary_keys"][0] if t.get("primary_keys") else _infer_pk(t["name"], t["columns"])
        if pk_col:
            pk_map[t["name"]] = pk_col

    fk_columns: dict[str, set[str]] = {}
    for rel in relationships:
        fk_columns.setdefault(rel["child_table"], set()).add(rel["child_column"])

    lines: list[str] = []
    lines.append("# Entity-Relationship Diagram")
    lines.append("")
    lines.append(
        "> Generated by Agentic Data Modeling · " f"source: `{report.get('catalog', '')}.{report.get('schema', '')}`"
    )
    lines.append("")
    lines.append("```mermaid")
    lines.append("erDiagram")

    # ---- Entity definitions ------------------------------------------------
    for t in tables:
        tname = t["name"]
        entity = _safe_entity(tname)
        pk_col = pk_map.get(tname)
        fk_cols = fk_columns.get(tname, set())

        lines.append(f"    {entity} {{")
        for col in t["columns"]:
            col_name = col["name"]
            mtype = _mermaid_type(col["type"])
            # sanitise attribute name: no spaces allowed in Mermaid
            safe_attr = re.sub(r"\s+", "_", col_name)
            if col_name == pk_col:
                marker = "PK"
            elif col_name in fk_cols:
                marker = "FK"
            else:
                marker = ""
            annotation = f' "{marker}"' if marker else ""
            lines.append(f"        {mtype} {safe_attr}{annotation}")
        lines.append("    }")
        lines.append("")

    # ---- Relationships -----------------------------------------------------
    for rel in relationships:
        parent = _safe_entity(rel["parent_table"])
        child = _safe_entity(rel["child_table"])
        rel_type = rel.get("type", "inferred")
        conf = rel.get("confidence", 0)
        label = "has" if rel_type == "explicit_fk" else f"~{int(conf * 100)}%"
        # one parent to zero-or-more children (standard FK cardinality)
        lines.append(f'    {parent} ||--o{{ {child} : "{label}"')

    lines.append("```")
    lines.append("")

    # ---- Summary table -----------------------------------------------------
    lines.append("## Tables")
    lines.append("")
    lines.append("| Table | Columns | Primary Key | Relationships |")
    lines.append("|-------|---------|-------------|--------------|")
    for t in tables:
        tname = t["name"]
        ncols = len(t["columns"])
        pk = pk_map.get(tname, "—")
        fk_count = sum(1 for r in relationships if r["child_table"] == tname)
        ref_count = sum(1 for r in relationships if r["parent_table"] == tname)
        rels_str = f"{fk_count} FK out · {ref_count} referenced"
        lines.append(f"| `{tname}` | {ncols} | `{pk}` | {rels_str} |")
    lines.append("")

    # ---- Relationships table -----------------------------------------------
    if relationships:
        lines.append("## Relationships")
        lines.append("")
        lines.append("| Child Table | Column | Parent Table | Column | Type | Confidence |")
        lines.append("|-------------|--------|--------------|--------|------|-----------|")
        for rel in relationships:
            parent_col = _resolve_parent_column(rel["parent_table"], rel["parent_column"], tables_by_name)
            lines.append(
                f"| `{rel['child_table']}` | `{rel['child_column']}` "
                f"| `{rel['parent_table']}` | `{parent_col}` "
                f"| {rel.get('type', '—')} | {rel.get('confidence', 0):.0%} |"
            )
        lines.append("")

    # ---- AI analysis summary -----------------------------------------------
    ai = report.get("ai_analysis")
    if isinstance(ai, dict):
        lines.append("## AI Analysis")
        lines.append("")

        recommendations = ai.get("modeling_recommendations", [])
        if recommendations:
            lines.append("### Modeling Recommendations")
            lines.append("")
            for rec in recommendations:
                lines.append(f"- {rec}")
            lines.append("")

        dq = ai.get("data_quality", [])
        if dq:
            lines.append("### Data Quality Findings")
            lines.append("")
            lines.append("| Table | Issue | Detail |")
            lines.append("|-------|-------|--------|")
            for finding in dq:
                tbl = finding.get("table", "—")
                issue = finding.get("issue_type", "—")
                detail = finding.get("detail", "—")
                lines.append(f"| `{tbl}` | {issue} | {detail} |")
            lines.append("")

        logical = ai.get("logical_model", {})
        subject_areas = logical.get("subject_areas", {})
        if subject_areas:
            lines.append("### Logical Model — Subject Areas")
            lines.append("")
            lines.append("| Subject Area | Tables |")
            lines.append("|---|---|")
            for area, tbl_list in subject_areas.items():
                tbls = ", ".join(f"`{t}`" for t in tbl_list) if isinstance(tbl_list, list) else str(tbl_list)
                lines.append(f"| {area} | {tbls} |")
            lines.append("")

        physical = ai.get("physical_model", {})
        denorms = physical.get("denormalizations", [])
        indexes = physical.get("recommended_indexes", [])
        if denorms or indexes:
            lines.append("### Physical Model")
            lines.append("")
            if denorms:
                lines.append("**Denormalization Suggestions:**")
                lines.append("")
                for d in denorms:
                    lines.append(f"- {d}")
                lines.append("")
            if indexes:
                lines.append("**Recommended Indexes:**")
                lines.append("")
                for idx in indexes:
                    lines.append(f"- {idx}")
                lines.append("")

    return "\n".join(lines)


