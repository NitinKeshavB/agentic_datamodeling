def erwin_assessment(report: dict[str, Any]) -> str:
    """Return a plain-text ERwin import readiness assessment."""
    tables = report.get("tables", [])
    rels = report.get("relationships", [])
    missing_pks = [t["name"] for t in tables if not t.get("primary_keys")]

    lines = [
        "ERwin Import Readiness",
        "=" * 50,
        "",
        "ERwin Data Modeler reverse-engineers from a SQL DDL file.",
        "The generated .sql file IS the correct ERwin input.",
        "",
        "Steps to import into ERwin:",
        "  1. Open ERwin Data Modeler",
        "  2. File → Reverse Engineer → From Script",
        "  3. Select database: 'Databricks' or 'Generic ANSI SQL'",
        "  4. Browse to the generated .sql file",
        "  5. ERwin reads CREATE TABLE + CONSTRAINT statements",
        "     and builds the ERD automatically.",
        "",
        "Current JSON coverage:",
        f"  Tables           : {len(tables)}",
        f"  Relationships    : {len(rels)}",
        f"  Tables w/o decl PK: {len(missing_pks)} ({', '.join(missing_pks) or 'none'})",
        "    → PKs are inferred by naming convention in the DDL",
        "",
        "Limitations of the JSON alone (why DDL is needed):",
        "  - ERwin cannot read the raw JSON directly",
        "  - ERwin needs CONSTRAINT syntax in the DDL",
        "  - The .sql file generated here contains all CREATE TABLE,",
        "    PRIMARY KEY (NOT ENFORCED), and FOREIGN KEY statements",
        "    that ERwin parses to reconstruct the entity-relationship diagram.",
        "",
        "Recommended ERwin target dialect: 'Databricks' (ERwin 2021+)",
        "Fallback: 'Generic ANSI SQL' (removes USING DELTA / NOT ENFORCED)",
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------


def generate_from_file(
    report_path: str,
    target_catalog: str | None = None,
    output_sql: str | None = None,
    output_erwin_notes: str | None = None,
    output_mermaid: str | None = None,
) -> tuple[str, str, str]:
    """Read a discovery JSON report and write DDL + ERwin notes + Mermaid ER diagram files."""
    report = json.loads(Path(report_path).read_text())

    ddl = generate_databricks_ddl(report, target_catalog=target_catalog)
    notes = erwin_assessment(report)
    diagram = generate_mermaid_er_diagram(report)

    base = Path(report_path)
    sql_path = output_sql or str(base.with_suffix(".sql"))
    notes_path = output_erwin_notes or str(base.with_suffix(".erwin_notes.txt"))
    mermaid_path = output_mermaid or str(base.with_suffix(".er_diagram.md"))

    Path(sql_path).write_text(ddl)
    Path(notes_path).write_text(notes)
    Path(mermaid_path).write_text(diagram)

    return sql_path, notes_path, mermaid_path
