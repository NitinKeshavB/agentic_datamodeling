"""Generate Databricks Unity Catalog DDL with PK/FK constraints from a discovery report."""

from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any

# ---------------------------------------------------------------------------
# Type mapping: source types → Databricks SQL types
# ---------------------------------------------------------------------------

_TYPE_MAP: dict[str, str] = {
    "bigint": "BIGINT",
    "int": "INT",
    "integer": "INT",
    "smallint": "SMALLINT",
    "tinyint": "TINYINT",
    "long": "BIGINT",
    "double": "DOUBLE",
    "float": "FLOAT",
    "decimal": "DECIMAL(18,2)",
    "numeric": "DECIMAL(18,2)",
    "string": "STRING",
    "varchar": "STRING",
    "char": "STRING",
    "text": "STRING",
    "boolean": "BOOLEAN",
    "bool": "BOOLEAN",
    "timestamp": "TIMESTAMP",
    "timestamp_ntz": "TIMESTAMP_NTZ",
    "date": "DATE",
    "binary": "BINARY",
    "array": "ARRAY<STRING>",
    "map": "MAP<STRING,STRING>",
    "struct": "STRUCT<>",
}

# MonthlyRent and similar currency columns get DECIMAL instead of BIGINT
_CURRENCY_COLUMNS = {"monthlyrent", "rent", "price", "amount", "cost", "fee", "balance"}


def _map_type(col_name: str, raw_type: str) -> str:
    """Map source type to Databricks SQL type, with currency-column overrides."""
    if col_name.lower().replace("_", "") in _CURRENCY_COLUMNS and raw_type.lower() == "bigint":
        return "DECIMAL(18,2)"
    base = raw_type.lower().split("(")[0].strip()
    return _TYPE_MAP.get(base, raw_type.upper())


# ---------------------------------------------------------------------------
# PK inference
# ---------------------------------------------------------------------------


def _infer_pk(table_name: str, columns: list[dict]) -> str | None:
    """Infer primary key column: prefer {TableName}ID, then ID, then first BIGINT."""
    col_names = [c["name"] for c in columns]
    candidates = [
        f"{table_name}ID",
        f"{table_name}Id",
        f"{table_name}_id",
        "ID",
        "Id",
        "id",
        "SNO",
    ]
    for candidate in candidates:
        if candidate in col_names:
            return candidate
    # fall back to first bigint/int column
    for col in columns:
        if col["type"].lower() in ("bigint", "int", "integer", "long"):
            return col["name"]
    return col_names[0] if col_names else None


def _resolve_parent_column(parent_table: str, parent_col_hint: str, tables_by_name: dict) -> str:
    """Resolve the actual parent PK column (relationship detector may emit 'id')."""
    if parent_col_hint.lower() != "id":
        return parent_col_hint
    parent_meta = tables_by_name.get(parent_table)
    if parent_meta:
        return _infer_pk(parent_table, parent_meta["columns"]) or parent_col_hint
    return parent_col_hint


# ---------------------------------------------------------------------------
# DDL builder
# ---------------------------------------------------------------------------


def generate_databricks_ddl(report: dict[str, Any], target_catalog: str | None = None) -> str:
    """
    Generate Databricks Unity Catalog DDL from a discovery report JSON.

    Produces:
      - CREATE TABLE IF NOT EXISTS with NOT NULL on PK/FK columns
      - NOT ENFORCED PRIMARY KEY constraints (inline)
      - NOT ENFORCED FOREIGN KEY constraints (ALTER TABLE, after creates)
      - USING DELTA + COMMENT
      - OPTIMIZE / ZORDER hints as comments
    """
    catalog = target_catalog or report.get("catalog", "target_catalog")
    schema = report.get("schema", "target_schema")
    tables = report.get("tables", [])
    relationships = report.get("relationships", [])

    tables_by_name = {t["name"]: t for t in tables}

    # Collect FKs per child table for inline awareness
    fk_columns: dict[str, set[str]] = {}
    for rel in relationships:
        child = rel["child_table"]
        fk_columns.setdefault(child, set()).add(rel["child_column"])

    # Collect inferred PKs
    pk_map: dict[str, str] = {}
    for t in tables:
        pk_col = t["primary_keys"][0] if t.get("primary_keys") else _infer_pk(t["name"], t["columns"])
        if pk_col:
            pk_map[t["name"]] = pk_col

    lines: list[str] = []

    lines.append("-- ============================================================")
    lines.append(f"-- Databricks DDL — {catalog}.{schema}")
    lines.append("-- Generated by Agentic Data Modeling (adm)")
    lines.append("-- Constraints are NOT ENFORCED (informational) per Databricks")
    lines.append("-- best-practice for optimizer hints and BI auto-join discovery.")
    lines.append("-- ============================================================")
    lines.append("")
    lines.append(f"USE CATALOG `{catalog}`;")
    lines.append(f"USE SCHEMA `{schema}`;")
    lines.append("")

    # ---- CREATE TABLE statements -----------------------------------------
    for t in tables:
        tname = t["name"]
        cols = t["columns"]
        pk_col = pk_map.get(tname)
        fk_cols = fk_columns.get(tname, set())
        comment = t.get("comment") or f"{tname} table"

        lines.append("-- ------------------------------------------------------------")
        lines.append(f"-- {tname}")
        lines.append("-- ------------------------------------------------------------")
        lines.append(f"CREATE TABLE IF NOT EXISTS `{catalog}`.`{schema}`.`{tname}` (")

        col_defs = []
        for col in cols:
            col_name = col["name"]
            db_type = _map_type(col_name, col["type"])

            # NOT NULL on PK and FK columns (observed 0% nulls in source)
            is_key = (col_name == pk_col) or (col_name in fk_cols)
            nullability = "NOT NULL" if is_key else "NULL"

            # Escape column names with spaces or reserved words
            safe_name = f"`{col_name}`" if not re.match(r"^[A-Za-z_][A-Za-z0-9_]*$", col_name) else col_name
            col_defs.append(f"    {safe_name:<30} {db_type:<20} {nullability}")

        # Inline PK constraint
        if pk_col:
            col_defs.append(f"    CONSTRAINT pk_{tname} PRIMARY KEY ({pk_col}) NOT ENFORCED")

        lines.append(",\n".join(col_defs))
        lines.append(")")
        lines.append("USING DELTA")
        lines.append(f"COMMENT '{comment}';")
        lines.append("")

    # ---- ALTER TABLE for FK constraints -----------------------------------
    lines.append("")
    lines.append("-- ============================================================")
    lines.append("-- Foreign Key Constraints (NOT ENFORCED — informational)")
    lines.append("-- ============================================================")
    lines.append("")

    for rel in relationships:
        child_table = rel["child_table"]
        child_col = rel["child_column"]
        parent_table = rel["parent_table"]
        parent_col = _resolve_parent_column(parent_table, rel["parent_column"], tables_by_name)
        conf = rel.get("confidence", 0)
        rel_type = rel.get("type", "inferred")

        constraint_name = f"fk_{child_table}_{child_col}_{parent_table}".lower()
        lines.append(f"-- [{rel_type}] confidence={conf:.0%}")
        lines.append(f"ALTER TABLE `{catalog}`.`{schema}`.`{child_table}`")
        lines.append(f"  ADD CONSTRAINT {constraint_name}")
        lines.append(f"  FOREIGN KEY ({child_col})")
        lines.append(f"  REFERENCES `{catalog}`.`{schema}`.`{parent_table}` ({parent_col})")
        lines.append("  NOT ENFORCED;")
        lines.append("")

    # ---- NOT NULL enforcement via ALTER -----------------------------------
    lines.append("")
    lines.append("-- ============================================================")
    lines.append("-- Enforce NOT NULL on key columns (safe — 0% nulls observed)")
    lines.append("-- ============================================================")
    lines.append("")
    for tname, pk_col in pk_map.items():
        lines.append(f"ALTER TABLE `{catalog}`.`{schema}`.`{tname}` " f"ALTER COLUMN {pk_col} SET NOT NULL;")
    lines.append("")

    # ---- Optimization hints -----------------------------------------------
    lines.append("-- ============================================================")
    lines.append("-- OPTIMIZE / ZORDER hints (run after data load)")
    lines.append("-- ============================================================")
    lines.append("")
    for rel in relationships:
        child_table = rel["child_table"]
        child_col = rel["child_column"]
        lines.append(f"-- OPTIMIZE `{catalog}`.`{schema}`.`{child_table}` " f"ZORDER BY ({child_col});")

    lines.append("")
    lines.append("-- End of DDL")
    return "\n".join(lines)


